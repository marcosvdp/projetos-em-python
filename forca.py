import random

def jogar():
    boas_vindas()
    palavra_secreta = escolha_da_palavra()

    letras_acertadas = ['_' for letra in palavra_secreta]
    print("A palavra é " + str(letras_acertadas))
    print("Você tem 6 tentativas, boa sorte!")

    erros = 0
    enforcou = False
    acertou = False

    while (not enforcou and not acertou):
        chute = solicita_chute()

        if (chute in palavra_secreta):
            chutes_corretos(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if (acertou):
        mensagem_vitoria()
    else:
        mensagem_derrota(palavra_secreta)



def boas_vindas():
    print("**************************")
    print("Seja bem vindo e boa sorte")
    print("**************************")

def escolha_da_palavra():
    arquivo = open("banco de palavras.txt", "r")
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()
    return palavra_secreta

def solicita_chute():
    chute = input("Qual Letra? ")
    chute = chute.strip().upper()
    return chute

def chutes_corretos(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1

